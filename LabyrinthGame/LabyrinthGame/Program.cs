            ConsoleKeyInfo ck;

            do
            {
               ck = Console.ReadKey(intercept: true);

                switch (ck.Key)
                {

                    case ConsoleKey.Escape:
                        exit = true;
                        GameOver();
                        break;
                    case ConsoleKey.W:
                        Console.Beep(270, 100);
                        Player1.MoveUp();
                        Console.Clear();
                        f.PrintField();

                        break;
                    case ConsoleKey.S:
                        Console.Beep(270, 100);
                        Player1.MoveDown();
                        Console.Clear();
                        f.PrintField();
                        break;
                    case ConsoleKey.A:
                        Console.Beep(270, 100);
                        Player1.MoveLeft();
                        Console.Clear();
                        f.PrintField();
                        break;
                    case ConsoleKey.D:
                        Console.Beep(270, 100);
                        Player1.MoveRight();
                        if (Program.exit == true) {

                            break; }
                        Console.Clear();
                        f.PrintField();

                        break;
                    default:
                        break;
                }
                
            }
            while (!exit );
        }

